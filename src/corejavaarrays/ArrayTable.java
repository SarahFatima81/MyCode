package corejavaarrays;

public class ArrayTable {

	public static void main(String[] args) {
		System.out.println("Index \t Table");
		
		int ageList[] = {22, 45, 60, 18, 32, 75};
		
		for(int counter = 0; counter < ageList.length; counter++){
			System.out.println(counter + "  \t  " + ageList[counter]);
		}
		
		
		
		

	}

}
