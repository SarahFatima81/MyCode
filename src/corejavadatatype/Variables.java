package corejavadatatype;

public class Variables {

	public static void main(String args[]){
		
		double dist = 5.5;
				
		System.out.print("The distance is ");
		System.out.print(dist);
		System.out.println(" miles to my house");
		
		
		int anum = 3;
		
		System.out.print("I have ");
		System.out.print(anum);
		System.out.println(" apples");
		
		float wt = 25.5f;
		
		System.out.print("The bag of rice weighs ");
		System.out.print(wt);
		System.out.println(" kilos");
		
	}
}
